public class CH03_02 {
    public static void main(String args[]) {
        int a = 30, b = 53;

        System.out.printf("a = %d b = %d \n", a, b);
        System.out.printf("-----------------------\n");
        System.out.println("a > b = " + (a > b));
        System.out.println("a < b = " + (a < b));
        System.out.println("a >= b = " + (a >= b));
        System.out.println("a <= b = " + (a <= b));
        System.out.println("a == b = " + (a == b));
        System.out.println("a != b = " + (a != b));
    }
}
